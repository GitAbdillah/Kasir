    private void tampil_tabel(){
    tm = new DefaultTableModel(null, new Object[]{"ID","Nama Pelanggan","Alamat","Nomor Handphone"});
    tabel.setModel(tm);
    tm.getDataVector().removeAllElements();
    try{
    PreparedStatement s = con.prepareStatement("select * from pelanggan");
    ResultSet r = s.executeQuery();
    while(r.next()){
    Object[] data = {
    r.getString(1),
    r.getString(2),
    r.getString(3),
    r.getString(4)
    };
    tm.addRow(data);
    }
    }catch(Exception e){
    System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
    }
    }
