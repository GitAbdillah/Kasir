    public void tambah(){
    try{
    PreparedStatement ps = con.prepareStatement("insert into pelanggan values(?,?,?,?)");
    ps.setString(1,id.getText());
    ps.setString(2,np.getText());
    ps.setString(3,alamat.getText());
    ps.setString(4,no.getText());
    ps.executeUpdate();
    JOptionPane.showMessageDialog(this, "Pelanggan telah ditambahkan");
    tampil_tabel();
    }catch(Exception e){
    System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
    }
    }
    public void edit(){
    try{
    PreparedStatement ps = con.prepareStatement("update pelanggan set nama=?, alamat=?,nomor=? where id_pelanggan=?");
    ps.setString(1,np.getText());
    ps.setString(2,alamat.getText());
    ps.setString(3,no.getText());
    ps.setString(4,id.getText());
    ps.executeUpdate();
    JOptionPane.showMessageDialog(this, "Data berhasil diubah");
    tampil_tabel();
    }catch(Exception e){
    System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
    }
    }
    public void hapus(){
    int ok = JOptionPane.showConfirmDialog(null, "Anda yakin akan menghapus pelanggan yang bernama " + np.getText()+"","Konfirmasi", JOptionPane.YES_NO_CANCEL_OPTION);
    if(ok==0){
    try{
    PreparedStatement ps= con.prepareStatement("delete FROM pelanggan where id_pelanggan=?");
    ps.setString(1,id.getText());
    ps.executeUpdate();
    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
    tampil_tabel();
    }catch(Exception e){
    System.out.print("ERROR KUERI KE DATABAE:\n" + e + "\n\n");
    }
    }
    }
    public void klik(){
    int i = tabel.getSelectedRow();
        if(i == -1){
        return;
        }
        String idpel=(String)tm.getValueAt(i,0);
        id.setText(idpel);
        String nama=(String)tm.getValueAt(i,1);
        np.setText(nama);
        String alamatt=(String)tm.getValueAt(i,2);
        alamat.setText(alamatt);
        String nomor = (String) tm.getValueAt(i,3);
        no.setText(nomor);
    }
