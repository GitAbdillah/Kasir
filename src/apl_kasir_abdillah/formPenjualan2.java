       public void tambah(){
        try{
        PreparedStatement ps = con.prepareStatement("insert into penjualan values(?,?,?,?)");
        ps.setString(1,id.getText());
        ps.setString(2,tgl);
        ps.setString(3,total.getText());
        ps.setString(4,idp.getSelectedItem().toString());
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Transaksi telah ditambahkan");
        tampil_tabel();
        }catch(Exception e){
        System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
        }
        public void edit(){
        try{
        PreparedStatement ps = con.prepareStatement("update penjualan set tanggal_penjualan=?, total=?,id_pelanggan=? where id_penjualan=?");
        ps.setString(1,tanggal.getDateFormatString());
        ps.setString(2,total.getText());
        ps.setString(3,idp.getSelectedItem().toString());
        ps.setString(4,id.getText());
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil diubah");
        tampil_tabel();
        }catch(Exception e){
        System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
        }
        public void hapus(){
        int ok = JOptionPane.showConfirmDialog(null, "Anda yakin akan menghapus transaksi di tanggal " +tanggal.getDateFormatString()+"","Konfirmasi", JOptionPane.YES_NO_CANCEL_OPTION);
        if(ok==0){
        try{
        PreparedStatement ps= con.prepareStatement("delete FROM penjualan where id_penjualan=?");
        ps.setString(1,id.getText());
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
        tampil_tabel();
        }catch(Exception e){
        System.out.print("ERROR KUERI KE DATABAE:\n" + e + "\n\n");
        }
        }
        }
        public void klik(){
        int i = tabel.getSelectedRow();
            if(i == -1){
            return;
            }
            String idproduk=(String)tm.getValueAt(i,0);
            id.setText(idproduk);
            String namaBarang=(String)tm.getValueAt(i,1);
            tanggal.setDateFormatString(namaBarang);
            String Harga=(String)tm.getValueAt(i,2);
            total.setText(Harga);
            String Stock = (String) tm.getValueAt(i,3);
            idp.setSelectedItem(Stock);
        }
