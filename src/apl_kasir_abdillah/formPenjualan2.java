    private void tampil_tabel(){
    tm = new DefaultTableModel(null, new Object[]{"ID Penjualan","Tanggal","Total","ID Pelanggan"});
    tabel.setModel(tm);
    tm.getDataVector().removeAllElements();
    try{
    PreparedStatement s = con.prepareStatement("select * from penjualan");
    ResultSet r = s.executeQuery();
    while(r.next()){
    Object[] data = {
    r.getString(1),
    r.getString(2),
    r.getString(3),
    r.getString(4)
    };
    tm.addRow(data);
    }
    }catch(Exception e){
    System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
    }
    }
    private void ambilData() {
        try {
            PreparedStatement s = con.prepareStatement("SELECT * FROM pelanggan");
            ResultSet r = s.executeQuery();
            
            while (r.next()) {
            idp.addItem(r.getString("id_pelanggan"));
            }
        }catch (Exception e) {
            
        }
    }
