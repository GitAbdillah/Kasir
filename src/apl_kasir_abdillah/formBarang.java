     public void tambah(){
    try{
    PreparedStatement ps = con.prepareStatement("insert into produk values(?,?,?,?)");
    ps.setString(1,id.getText());
    ps.setString(2,np.getText());
    ps.setString(3,harga.getText());
    ps.setString(4,stok.getText());
    ps.executeUpdate();
    JOptionPane.showMessageDialog(this, "Barang telah ditambahkan");
    tampil_tabel();
    }catch(Exception e){
    System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
    }
    }
    public void edit(){
    try{
    PreparedStatement ps = con.prepareStatement("update produk set nama_produk=?, harga=?,stok=? where id_produk=?");
    ps.setString(1,np.getText());
    ps.setString(2,harga.getText());
    ps.setString(3,stok.getText());
    ps.setString(4,id.getText());
    ps.executeUpdate();
    JOptionPane.showMessageDialog(this, "Data berhasil diubah");
    tampil_tabel();
    }catch(Exception e){
    System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
    }
    }
    public void hapus(){
    int ok = JOptionPane.showConfirmDialog(null, "Anda yakin akan menghapus barang " + np.getText()+"","Konfirmasi", JOptionPane.YES_NO_CANCEL_OPTION);
    if(ok==0){
    try{
    PreparedStatement ps= con.prepareStatement("delete FROM produk where id_produk=?");
    ps.setString(1,id.getText());
    ps.executeUpdate();
    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
    tampil_tabel();
    }catch(Exception e){
    System.out.print("ERROR KUERI KE DATABAE:\n" + e + "\n\n");
    }
    }
    }
    public void klik(){
    int i = tabel.getSelectedRow();
        if(i == -1){
        return;
        }
        String idproduk=(String)tm.getValueAt(i,0);
        id.setText(idproduk);
        String namaBarang=(String)tm.getValueAt(i,1);
        np.setText(namaBarang);
        String Harga=(String)tm.getValueAt(i,2);
        harga.setText(Harga);
        String Stock = (String) tm.getValueAt(i,3);
        stok.setText(Stock);
    }

