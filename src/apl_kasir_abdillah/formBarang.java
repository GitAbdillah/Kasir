    private void stokKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_stokKeyTyped
        // TODO add your handling code here:
        char karakter = evt.getKeyChar();
        if(!(((karakter >=  '0') && (karakter <= '9' || (karakter == KeyEvent.VK_BACKSPACE) || (karakter == KeyEvent.VK_DELETE))))){
            getToolkit().beep();
            evt.consume();
        }
        if ( stok.getText().length() == 11 ) {
        evt.consume();
        }
    }//GEN-LAST:event_stokKeyTyped

